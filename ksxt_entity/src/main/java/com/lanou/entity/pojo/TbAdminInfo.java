package com.lanou.entity.pojo;

import javax.persistence.*;

@Table(name = "tb_admin_info")
public class TbAdminInfo {
    @Id
    @Column(name = "id")
    @GeneratedValue(generator = "JDBC")
    private Integer id;

    /**
     * 管理员id
     */
    @Id
    @Column(name = "admin")
    private Integer admin;

    /**
     * 姓名
     */
    @Column(name = "name")
    private String name;

    /**
     * 电话
     */
    @Column(name = "phone")
    private String phone;

    /**
     * 邮件
     */
    @Column(name = "email")
    private String email;

    /**
     * 备注
     */
    @Column(name = "notes")
    private String notes;

    /**
     * @return id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * 获取管理员id
     *
     * @return admin - 管理员id
     */
    public Integer getAdmin() {
        return admin;
    }

    /**
     * 设置管理员id
     *
     * @param admin 管理员id
     */
    public void setAdmin(Integer admin) {
        this.admin = admin;
    }

    /**
     * 获取姓名
     *
     * @return name - 姓名
     */
    public String getName() {
        return name;
    }

    /**
     * 设置姓名
     *
     * @param name 姓名
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * 获取电话
     *
     * @return phone - 电话
     */
    public String getTel() {
        return phone;
    }

    /**
     * 设置电话
     *
     * @param phone 电话
     */
    public void setTel(String phone) {
        this.phone = phone;
    }

    /**
     * 获取邮件
     *
     * @return email - 邮件
     */
    public String getEmail() {
        return email;
    }

    /**
     * 设置邮件
     *
     * @param email 邮件
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * 获取备注
     *
     * @return notes - 备注
     */
    public String getNotes() {
        return notes;
    }

    /**
     * 设置备注
     *
     * @param notes 备注
     */
    public void setNotes(String notes) {
        this.notes = notes;
    }
}