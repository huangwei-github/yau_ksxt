package com.lanou.entity.pojo;

import java.util.Date;
import javax.persistence.*;

@Table(name = "tb_student_info")
public class TbStudentInfo {
    @Id
    @Column(name = "id")
    @GeneratedValue(generator = "JDBC")
    private Integer id;

    /**
     * 学生id
     */
    @Id
    @Column(name = "student")
    private Integer student;

    /**
     * 姓名
     */
    @Column(name = "name")
    private String name;

    /**
     * 性别: 1(男) 2(女)
     */
    @Column(name = "gender")
    private Byte gender;

    /**
     * 出生日期
     */
    @Column(name = "birth")
    private Date birth;

    /**
     * 家庭住址
     */
    @Column(name = "addr")
    private String addr;

    /**
     * 民族
     */
    @Column(name = "nation")
    private String nation;

    /**
     * 身份证号
     */
    @Column(name = "identify")
    private String identify;

    /**
     * 手机号码
     */
    @Column(name = "phone")
    private String phone;

    /**
     * 电子邮箱
     */
    @Column(name = "email")
    private String email;

    /**
     * 备注
     */
    @Column(name = "notes")
    private String notes;

    /**
     * @return id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * 获取学生id
     *
     * @return student - 学生id
     */
    public Integer getStudent() {
        return student;
    }

    /**
     * 设置学生id
     *
     * @param student 学生id
     */
    public void setStudent(Integer student) {
        this.student = student;
    }

    /**
     * 获取姓名
     *
     * @return name - 姓名
     */
    public String getName() {
        return name;
    }

    /**
     * 设置姓名
     *
     * @param name 姓名
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * 获取性别: 1(男) 2(女)
     *
     * @return gender - 性别: 1(男) 2(女)
     */
    public Byte getGender() {
        return gender;
    }

    /**
     * 设置性别: 1(男) 2(女)
     *
     * @param gender 性别: 1(男) 2(女)
     */
    public void setGender(Byte gender) {
        this.gender = gender;
    }

    /**
     * 获取出生日期
     *
     * @return birth - 出生日期
     */
    public Date getBirth() {
        return birth;
    }

    /**
     * 设置出生日期
     *
     * @param birth 出生日期
     */
    public void setBirth(Date birth) {
        this.birth = birth;
    }

    /**
     * 获取家庭住址
     *
     * @return addr - 家庭住址
     */
    public String getAddr() {
        return addr;
    }

    /**
     * 设置家庭住址
     *
     * @param addr 家庭住址
     */
    public void setAddr(String addr) {
        this.addr = addr;
    }

    /**
     * 获取民族
     *
     * @return nation - 民族
     */
    public String getNation() {
        return nation;
    }

    /**
     * 设置民族
     *
     * @param nation 民族
     */
    public void setNation(String nation) {
        this.nation = nation;
    }

    /**
     * 获取身份证号
     *
     * @return identify - 身份证号
     */
    public String getIdentify() {
        return identify;
    }

    /**
     * 设置身份证号
     *
     * @param identify 身份证号
     */
    public void setIdentify(String identify) {
        this.identify = identify;
    }

    /**
     * 获取手机号码
     *
     * @return phone - 手机号码
     */
    public String getPhone() {
        return phone;
    }

    /**
     * 设置手机号码
     *
     * @param phone 手机号码
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

    /**
     * 获取电子邮箱
     *
     * @return email - 电子邮箱
     */
    public String getEmail() {
        return email;
    }

    /**
     * 设置电子邮箱
     *
     * @param email 电子邮箱
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * 获取备注
     *
     * @return notes - 备注
     */
    public String getNotes() {
        return notes;
    }

    /**
     * 设置备注
     *
     * @param notes 备注
     */
    public void setNotes(String notes) {
        this.notes = notes;
    }
}